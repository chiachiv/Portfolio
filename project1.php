        <h2 class="proj-title">UX Design</h2>
        <h3 class="proj-sub">Conceptual & Concrete Scenarios</h3>
        <section class="scenarios">
            <div class="scenario">
                <h3 class="p-title">Conceptual Model</h3>
                <p class="p-persona">The model involves a mobile app designed to help users recycle expired or near-expiry food into compost. Composting converts food waste into <strong>valuable fertilizer</strong>, which can be used in various ways. The app offers guidance on <strong>composting techniques</strong> and enables users to <strong>donate food</strong> to local associations that use the compost for community benefit.</p>
                <p class="p-persona">Users can create a personal profile within the app, access a <strong>forum</strong> filled with permaculture information, and interact with other users. The app also features a <strong>food donation</strong> function where users can specify food types and quantities, view available nearby associations on a map, and arrange for food delivery. All services are free of charge.</p>
            </div>
            <div class="scenario">
                <h3 class="p-title">Concrete Scenarios</h3>
                <p class="p-persona"><strong>Create Your Own Compost:</strong> Maureen has leftover food from a party that’s nearing expiration. She opens the CompostIt app on her phone and registers if she doesn’t have an account [1]. She then navigates to the forum section [2] to explore posts and advice on composting. Maureen can post questions and engage with the community [3].</p>
                <p class="p-persona"><strong>Donate Your Food Waste:</strong> Etienne has expired food at home and wants to avoid waste. He opens the CompostIt app, registers for an account [1], and goes to the donation section. He inputs the food type (vegetables) and quantity [2]. A map displays nearby associations [3], where he selects one to donate the food. The app confirms the donation details and Etienne takes the food to the chosen association [4].</p>
            <p class="p-persona"><strong>Notes:</strong> [1] need an account [2] a map to show where to bring your waste [3] select an association [4] bring the food waste.</p>
            </div>
        </section>
